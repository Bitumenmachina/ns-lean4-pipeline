def main():
    results = json.loads(Z3_RESULTS.read_text())
    OUT.parent.mkdir(parents=True, exist_ok=True)

    lines = []
    lines.append("import NSLean4.NSParams")
    lines.append("")
    lines.append("/-!")
    lines.append("  NSCerts.lean — Axioms from Z3-verified numerical certificates")
    lines.append("  Generated by s8_z3_to_lean.py from z3/results.json")
    lines.append("")
    lines.append("  Trust model: each axiom corresponds to a QF_LRA assertion in")
    lines.append("  z3/all_certs.smt2 that Z3 proved SAT. Re-audit by running:")
    lines.append("    python3 z3/run_z3.py")
    lines.append("-/")
    lines.append("")
    lines.append("namespace NS.Certs")
    lines.append("")

    sat_count = 0
    skip_count = 0

    for cert_name, result in results.items():
        if cert_name not in AXIOM_MAP:
            lines.append(f"-- SKIPPED: {cert_name} (no axiom mapping)")
            skip_count += 1
            continue

        info = AXIOM_MAP[cert_name]
        status = result["status"]

        lines.append(f"-- Z3 cert: {cert_name} [{status}]")
        lines.append(f"-- {info['desc']}")

        if status != "sat":
            lines.append(f"-- WARNING: certificate {status}, axiom NOT emitted")
            lines.append("")
            skip_count += 1
            continue

        if "custom" in info:
            lines.append(info["custom"])
        else:
            lines.append(f"axiom {info['name']} : {info['type']}")

        lines.append("")
        sat_count += 1

    lines.append("end NS.Certs")

    OUT.write_text("\n".join(lines) + "\n")
    print(f"Wrote {OUT}  ({OUT.stat().st_size} bytes)")
    print(f"  {sat_count} certificates → axioms")
    print(f"  {skip_count} skipped")


if __name__ == "__main__":
    main()
